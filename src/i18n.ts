export type Lang = 'zh' | 'en';

export const t = (lang: Lang, key: string): string => {
  const val = translations[lang]?.[key];
  if (typeof val === 'string') return val;
  return (translations.zh[key] as string) ?? key;
};

export const ta = (lang: Lang, key: string): string[] => {
  const val = translations[lang]?.[key];
  if (Array.isArray(val)) return val as string[];
  const zhVal = translations.zh[key];
  if (Array.isArray(zhVal)) return zhVal as string[];
  return [];
};

export const to = (lang: Lang, key: string): Record<string, string> => {
  const val = translations[lang]?.[key];
  if (val && typeof val === 'object' && !Array.isArray(val)) return val as Record<string, string>;
  const zhVal = translations.zh[key];
  if (zhVal && typeof zhVal === 'object' && !Array.isArray(zhVal)) return zhVal as Record<string, string>;
  return {};
};

